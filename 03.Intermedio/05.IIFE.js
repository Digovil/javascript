/**
 * IIFE Las expresiones de función ejecutadas inmediatamente
 * Immediately invoked function expression
 * Funciones que se ejecutan inmediatamente
 */

(function(){

    const nombreUsuario = "Dilan";
    console.log(nombreUsuario)
    
})();//El ultimo parentesis es lo que manda a llamar la función inmediatamente


// console.log(nombreUsuario) No se puede acceder a la variable